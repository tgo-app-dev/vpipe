inline constexpr const FontGlyph&
font5x7_glyph(char ch) noexcept
{
  auto u = static_cast<unsigned char>(ch);
  if (u < 0x20 || u > 0x7F) {
    return kFontMissing;
  }
  return kFont5x7[u - 0x20];
}

