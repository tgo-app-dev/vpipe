export function makeIcon(name, extraClass = '') {
  const grid = G[name] || G.pipeline;
  const root = el('span', { class: 'icon ' + extraClass });
  for (let r = 0; r < N; r++) {
    const row = grid[r] || '';
    for (let c = 0; c < N; c++) {
      const on = row[c] === '#';
      root.append(el('i', on ? { class: 'on' } : {}));
    }
  }
  return root;
}

